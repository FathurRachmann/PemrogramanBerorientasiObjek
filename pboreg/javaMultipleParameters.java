package com.pboreg;

public class javaMultipleParameters {
        static void myMethod(String fname, int age){
            System.out.println(fname + " is " + age);
        }
        public static void main(String[] args){
            myMethod("linda",5);
            myMethod("xiangling",8);
            myMethod("liang teh",31);
            myMethod("teh pucuk",21);
        }
}
